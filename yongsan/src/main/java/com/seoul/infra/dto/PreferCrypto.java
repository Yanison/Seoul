package com.seoul.infra.dto;

public class PreferCrypto {
	private Integer preferCryptoSeq;
	private Integer memberSeq;
	private Integer cryptoSeq;
	private Integer preferNy;
	
	
	public Integer getPreferCryptoSeq() {
		return preferCryptoSeq;
	}
	public void setPreferCryptoSeq(Integer preferCryptoSeq) {
		this.preferCryptoSeq = preferCryptoSeq;
	}
	public Integer getMemberSeq() {
		return memberSeq;
	}
	public void setMemberSeq(Integer memberSeq) {
		this.memberSeq = memberSeq;
	}
	public Integer getCryptoSeq() {
		return cryptoSeq;
	}
	public void setCryptoSeq(Integer cryptoSeq) {
		this.cryptoSeq = cryptoSeq;
	}
	public Integer getPreferNy() {
		return preferNy;
	}
	public void setPreferNy(Integer preferNy) {
		this.preferNy = preferNy;
	}
	
	

}
